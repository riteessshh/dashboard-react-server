import mongoose from "mongoose";

const rawDataSchema = new mongoose.Schema({
  _id: Number,
  end_year: String,
  intensity: Number,
  sector: String,
  topic: String,
  insight: String,
  url: String,
  region: String,
  start_year: String,
  impact: String,
  added: String,
  published: String,
  country: String,
  relevance: Number,
  pestle: String,
  source: String,
  title: String,
  likelihood: Number,
});

export const rawData = mongoose.model("rawData", rawDataSchema);
